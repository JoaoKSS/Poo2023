package br.edu.ifnmg.pessoa;

/**
 *
 * @author joaok
 */
public class Telefone {

    private Byte ddd;
    private Integer numero;

    public Byte getDdd() {
        return ddd;
    }

    public void setDdd(Byte ddd) {
        this.ddd = ddd;
    }

    public Integer getNumero() {
        return numero;
    }

    public void setNumero(Integer numero) {
        this.numero = numero;
    }

    @Override
    public String toString() {
        return "" + ddd  + numero;
    }
    
}
